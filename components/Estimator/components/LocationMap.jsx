export const LocationMap = () => {
  const { formData } = useContext(EstimateContext);
  const { mapLocation } = formData;

  const defaultCenter = { lat: 39.9526, lng: -75.1652 };
  const mapCenter = mapLocation
    ? { lat: mapLocation.lat, lng: mapLocation.lng }
    : defaultCenter;

  return (
    // The <LoadScript> wrapper is no longer needed here
    <GoogleMap
      mapContainerStyle={containerStyle}
      center={mapCenter}
      zoom={mapLocation ? 15 : 12}
      options={{
        styles: mapStyles,
        disableDefaultUI: true,
        zoomControl: true,
      }}
    >
      {mapLocation && <Marker position={mapCenter} />}
    </GoogleMap>
  );
};
